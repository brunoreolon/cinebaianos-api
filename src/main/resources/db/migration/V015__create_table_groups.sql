create table groups
(
    id                        BIGINT generated by default as identity,
    name                      varchar(255)       not null,
    tag                       varchar(6) unique  not null,
    slug                      varchar(50) unique not null,
    owner_id                  varchar(255)       not null,
    active                    boolean default true,
    visibility                varchar(30)        not null,
    join_policy               varchar(30)        not null,
    only_admin_add_movie      boolean default false,
    allow_global_votes        boolean default true,
    vote_change_deadline_days smallint           not null,
    movie_new_days            smallint           not null,
    invite_max_uses           smallint           not null,
    created_at                timestamp          not null,
    updated_at                timestamp          null,
    banned_by                 varchar(255) null,
    banned_at                 timestamp null,
    ban_reason                text null,
    expires_at                timestamp null,
    primary key (id),
    constraint fk_groups_owner foreign key (owner_id) references users (discord_id),
    constraint fk_groups_banned_by foreign key (banned_by) references users (discord_id)
);


