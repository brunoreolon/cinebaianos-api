create table group_movies
(
    id         bigint generated by default as identity,
    group_id   bigint       not null,
    movie_id   bigint       not null,
    chooser_id varchar(255) not null,
    date_added timestamp    not null,
    primary key (id),
    constraint fk_group_movie_group foreign key (group_id) references groups (id),
    constraint fk_group_movie_movie foreign key (movie_id) references movie (id),
    constraint fk_group_movie_chooser foreign key (chooser_id) references users (discord_id),
    constraint group_movie_key unique (group_id, movie_id)
);


